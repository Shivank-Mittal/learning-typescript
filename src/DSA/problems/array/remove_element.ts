// Example 1:

// Input: nums = [1,1,1,2,2,3]
// Output: 5, nums = [1,1,2,2,3,_]
// Explanation: Your function should return k = 5, with the first five elements of nums being 1, 1, 2, 2 and 3 respectively.
// It does not matter what you leave beyond the returned k (hence they are underscores).


export function removeElementIfMoreThenTwo(array:number[]) {

    const record = new Map<number, number>();

    for (let i = 0; i < array.length; i++) {
        const valueFromRecord = record.get(array[i]);
        if(!valueFromRecord ) record.set(array[i], 1);
        if(valueFromRecord && valueFromRecord < 2) record.set(array[i], 2)
    }

    array.length = 0
    record.forEach((v, k) => {
        v === 2?  array.push(k, k): array.push(k)
    })

    return array.length
}